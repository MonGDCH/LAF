<?php

/*
|--------------------------------------------------------------------------
| 获取应用实例
|--------------------------------------------------------------------------
| 这里获取应用实例
|
*/
$app = require __DIR__ . '/bootstrap.php';

/*
|--------------------------------------------------------------------------
| 注册应用钩子
|--------------------------------------------------------------------------
| 这里搭载应用钩子
|
*/
$app->definition( require(__DIR__.'/config/tags.php') );


/*
|--------------------------------------------------------------------------
| 定义应用启动模式
|--------------------------------------------------------------------------
| 这里设置应用是否为调试模式启动
|
*/
$app->debug( (strtolower(RUN_MODE) !== 'prd') );


/*
|--------------------------------------------------------------------------
| 初始化应用
|--------------------------------------------------------------------------
| 这里初始化应用
|
*/
$app->init();

return $app;